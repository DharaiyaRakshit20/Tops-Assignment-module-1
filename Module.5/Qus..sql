-- Ans : Data redundancy occurs when the same piece of data exists in multiple places, whereas data inconsistency is when the same data exists in 
--       different formats in multiple tables. Unfortunately, data redundancy can cause data inconsistency, which can provide a company with unreliable
--       and/or meaningless information.

