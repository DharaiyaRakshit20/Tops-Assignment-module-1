-- 4. 
-- Ans : Codd's twelve rules are a set of thirteen rules (numbered zero to twelve) proposed by Edgar F. Codd, a pioneer of the relational model for
--       databases, designed to define what is required from a database management system in order for it to be considered relational, i.e., 
--       a relational database management system (RDBMS).

